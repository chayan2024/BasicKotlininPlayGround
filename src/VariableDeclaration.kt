fun main() {

    //Normal Way to Declare variable

    var name="Mamata"
    var age=32
    var gender=true
    var salary=2000

    println("Name ::  ${name}")
    println("Age ::  ${age}")
    println("gender ::  ${gender}")
    println("salary ::  ${salary}")

    //Other Way to Declare variable

    var empName:String="Modi"
    var empAge:Int=30
    var empGender:Boolean=false
    var empSalary:Float=5000f

    println("Employee Name: ${empName}")
    println("Age:${empAge}")
    println("EmpGender:${empGender}")
    println("EmpSalaray:${empSalary}")

}